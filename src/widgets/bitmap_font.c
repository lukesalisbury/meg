cairo_status_t meg_bit_font_init( cairo_scaled_font_t  * scaled_font, cairo_t * cr, cairo_font_extents_t * metrics)
{
	metrics->ascent  = 1;
	metrics->descent = 0;
	return CAIRO_STATUS_SUCCESS;
}

cairo_status_t meg_bit_font_unicode_to_glyph( cairo_scaled_font_t * scaled_font, unsigned long unicode, unsigned long * glyph)
{
	unsigned long i;

	for (i = 32; i <= 128; i++)
	{
		if (i == unicode)
		{
			*glyph = i;
			return CAIRO_STATUS_SUCCESS;
		}
	}
	// Fall back to ?
	*glyph = 63;
	return CAIRO_STATUS_SUCCESS;
}

cairo_status_t meg_bit_font_render_glyph( cairo_scaled_font_t  *scaled_font, unsigned long glyph, cairo_t * cr, cairo_text_extents_t * metrics )
{
	int i;
	unsigned char *data;
	cairo_surface_t *image;
	cairo_pattern_t *pattern;
	cairo_matrix_t matrix;
	unsigned char byte;

	metrics->x_advance = 8.0/8.0;

	image = cairo_image_surface_create(CAIRO_FORMAT_A1, 8, 8);
	data = cairo_image_surface_get_data(image);

	for (i = 0; i < 8; i++)
	{
		byte = gfxPrimitivesFontdata[(glyph*8)+i];
		*data = BitReverseTable256[byte];
		data += cairo_image_surface_get_stride(image);
	}
	cairo_surface_mark_dirty(image);

	pattern = cairo_pattern_create_for_surface(image);
	cairo_matrix_init_identity( &matrix );
	cairo_matrix_scale( &matrix, 1.0/8.0, 1.0/8.0 );
	cairo_matrix_translate( &matrix, 0, -8 );
	cairo_matrix_invert( &matrix );
	cairo_pattern_set_matrix( pattern, &matrix);

	cairo_set_source( cr, pattern );
	cairo_mask( cr, pattern );

	cairo_pattern_destroy( pattern );
	cairo_surface_destroy( image );

	return CAIRO_STATUS_SUCCESS;
}

cairo_status_t meg_bit_font_face_create(cairo_font_face_t **out)
{
	cairo_font_face_t * user_font_face;
	user_font_face = cairo_user_font_face_create();
	cairo_user_font_face_set_init_func( user_font_face, meg_bit_font_init );
	cairo_user_font_face_set_render_glyph_func( user_font_face, meg_bit_font_render_glyph );
	cairo_user_font_face_set_unicode_to_glyph_func( user_font_face, meg_bit_font_unicode_to_glyph);

	*out = user_font_face;
	return CAIRO_STATUS_SUCCESS;
}
